package com.msb.learningspringframework.game;

public class MarioGame {
    public void up() {
        System.out.println("Jump");
    }
    public void down() {
        System.out.println("Goes into a hole");
    }
    public void left() {
        System.out.println("Goes back");
    }
    public void right() {
        System.out.println("Goes forward");
    }
}
